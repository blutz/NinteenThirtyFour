    <p><strong>What <em>not</em> to bring.</strong> Know what your child is bringing! Any of these items may earn them an early trip home.</p>
    <ul>
      <li><strong>Food or things that smell like food.</strong> Since we are in the forest, we are very careful about not attracting animals. Except for the first day's sack lunch, any type of food or food-scented items are not allowed. Campers will have a chance to throw away any leftover food once they arrive.</li>
      <li><strong>Weapons, fireworks, drugs, alcohol, controlled substances, tabacco, smoking/vaping products, matches, lighters, etc.</strong> Even if legal, these are not appropriate for our camp environment and some have the potential to cause forest fires.</li>
      <li><strong>Toys.</strong> Small stuffed friends are okay, but there is enough activity going on each day that toys aren't necessary and can be distracting.</li>
      <li><strong>Video games, music players, cell phones, and other expensive items.</strong> These items take away from the camp experience and may be damaged by the active, outdoor nature of camp. Cell phones will be collected on the first day and stored safely until the end of the session.</li>
      <li><strong>Markers or marking pens.</strong> Any supplies for arts &amp; crafts or journaling activities will be available at camp.</li>
