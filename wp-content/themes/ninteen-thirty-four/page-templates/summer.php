    <h4>Transportation</h4>
    <p>UCLA UniCamp runs our programs at our campsite in the San Bernardino National Forest. Open enrollment sessions include bus transportation from Los Angeles to/from our campsite. The exact location in Los Angeles will be confirmed with you over email prior to your session. Pickup locations for most sessions are at UCLA.</p>
    <p>Busses pick up campers around 9am on the first day of the session and drop off campers around 5pm on the last day.</p>
    <p><strong>Pickup times and locations vary by session and will be confirmed with you over email after registration.</strong></p>
    <h4>Cost</h4>
    <p>Our direct cost per camper is $600. However, through sponsorships, grants, and fundraising, we are able to lower the cost families pay. For open enrollment, the cost per camper is:</p>
    <ul>
      <li><strong>$199</strong> if qualified for free/reduced lunch</li>
      <li><strong>$300</strong> if not qualified</li>
    </ul>
    <p>We ask all families to complete an income eligibility form as part of the registration process. The forms qualify us for additional funding that helps us reduce the cost to you.</p>
    <p><strong>Campers with an active case with Los Angeles DCFS may qualify for a full campership.</strong> Contact us or have your social worker contact us at <a href='mailto:registration@unicamp.org'>registration@unicamp.org</a> for more information.</p>
    <h4>Other Information</h4>
    <p><strong>Can I send letters?</strong> Please drop off any letters (labeled with the camper's name and date you'd like to give it to them) with UniCamp staff on the first day of camp. Small care packages are okay as long as they do not have food.</p>
    <p><strong>Can I contact my camper?</strong> If you have an emergency and need to get in contact with your camper, call us at 310-208-8252 ext 767 (easily remembered as 844-UNICAMP ext SOS). This number is only connected during our summer season. We will reach out to you directly if we have any questions or if there are any emergencies at camp. Since camper independence is one of our major outcomes (and in some cases can cause homesickness), we strongly discourage phone calls to campers.</p>
    <p><strong>What are the age requirements?</strong> Campers should be between 10 and 17 years old to participate in UCLA UniCamp.</p>
    <p><strong>What are the sleeping arrangements?</strong> Campers will sleep in a cabin with up to 9 other similarly-aged campers and 2 counselors of the same gender. Cabins are open air and have a bathroom and sink.</p>
    <p><strong>How are campers supervised?</strong> All UCLA UniCamp counselors are volunteers who go through four months of training and are overseen by more experienced counselors and UniCamp full-time staff. We follow the "rule of three" where campers are always with two or more people, at least one of whom must be an adult. We have an average supervision ratio of 3 campers to 1 counselor.</p>
